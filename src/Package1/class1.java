package Package1;

public class class1 {
	
	public int a=9;
	private int b=3;
	protected int c=7;
	int r=5;
	
	public static void main(String[] args) {
		class1 obj=new class1();
		System.out.println(obj.a);
		System.out.println(obj.b);
		System.out.println(obj.c);
		System.out.println(obj.r);
	}

}
