package com.flm;

import java.util.Scanner;

public class Switch {
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		int a=sc.nextInt();
		switch(a) {
			case 1:
				System.out.println("Postive");
				break;
			case -1:
				System.out.println("neg");
				break;
			case 0:
				System.out.println("zero");
				break;
			default:
				System.out.println("Inavid");
		
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
